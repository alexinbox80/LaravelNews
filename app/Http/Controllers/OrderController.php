    {
        $order = new Order(
            $request->validated()
        );

        if($order->save()) {
            return redirect()->route('order.index')
                ->with('success',  __('messages.user.orders.create.success'));
        }

        return back()->with('error', __('messages.user.orders.create.fail'));
    }

